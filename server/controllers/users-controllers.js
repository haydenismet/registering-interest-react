const getByUserId = (req, res, next) => {
  const userId = req.params.uid;
  const user = DUMMY_USERS.find((u) => {
    return u.id.$oid === userId;
  });
  console.log("GET Request in User ID");

  if (!user) {
    return next(new HttpError("Could not find User ID", 404));
  }

  res.json({ user });
};

const getByUserFilter = (req, res, next) => {
  const filters = req.query;

  const filteredUsers = DUMMY_USERS.filter((user, i) => {
    let isFiltered = true;
    for (let key in filters) {
      isFiltered =
        isFiltered && user[key].toLowerCase() === filters[key].toLowerCase();
    }
    return isFiltered;
  });
  console.log("Get request by user filter");
  if (!filteredUsers || filteredUsers.length === 0) {
    return next(
      new HttpError("Could not find users based on your search query", 404)
    );
  }

  res.json(filteredUsers);
};

const signup = (req, res, next) => {
  const { name, email, password } = req.body;

  const existingUser = DUMMY_USERS.find(
    (existing) => existing.email.toLowerCase() === email.toLowerCase()
  );

  if (existingUser) {
    throw new HttpError("Email already exists", 422);
  }

  const createdUser = {
    id: uuidv4(),
    name: name,
    email: email,
    password: password,
  };

  DUMMY_USERS.push(createdUser);

  res.status(201).json({ user: createdUser });
};

const login = (req, res, next) => {
  const { email, password } = req.body;
  const identifiedUser = DUMMY_USERS.find(
    (u) => u.email.toLowerCase() === email.toLowerCase()
  );

  if (!identifiedUser || identifiedUser.password !== password) {
    throw new HttpError("Could not identify user", 401);
  }

  res.json({ message: "Logged in!" });
};

const patchUser = (req, res, next) => {
  const user = req.params.uid;
  const userUpdates = req.body;
  const userIndex = DUMMY_USERS.findIndex((u) => u.id.$oid === user);
  let userToUpdate = { ...DUMMY_USERS.find((u) => u.id.$oid === user) };
  let userToUpdateKeys = Object.keys(userToUpdate);
  let userUpdatesKeys = Object.keys(userUpdates);
  userUpdatesKeys.forEach((key) => {
    userToUpdateKeys.forEach((keyTwo) => {
      if (key === keyTwo) {
        userToUpdate[key] = userUpdates[key];
      }
    });
  });
  DUMMY_USERS[userIndex] = userToUpdate;

  res.status(200).json({ patchedUser: userToUpdate });
};

